  def icon_in_circle(name, type: :default, size: :md, options: {})
    circle_size = case size
    when :sm then "icon-circle-sm"
    when :md then "icon-circle-md"
    when :lg then "icon-circle-lg"
    when :xl then "icon-circle-xl"
    else "icon-circle-md"
    end

    circle_type = case type.to_s
    when "agenda" then "icon-circle-agenda"
    when "minutes" then "icon-circle-minutes"
    when "brand" then "icon-circle-brand"
    else "icon-circle-default"
    end

    icon_size = case size
    when :sm then "w-4 h-4"
    when :md then "w-5 h-5"
    when :lg then "w-6 h-6"
    when :xl then "w-8 h-8"
    else "w-5 h-5"
    end

    additional_classes = options[:class] || ""

    content_tag :span, class: "icon-circle #{circle_size} #{circle_type} #{additional_classes}".strip do
      icon(name, size: icon_size)
    end
  end

  def document_type_icon_with_circle(type, size: :md)
    icon_name = case type.to_s.downcase
    when "agenda" then "agenda"
    when "minutes" then "minutes"
    else "document"
    end

    icon_in_circle(icon_name, type: type, size: size)
  end
